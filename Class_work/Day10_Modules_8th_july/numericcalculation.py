#function to calculate addition of two number
def calculate_addition(num1,num2):
    return (num1+num2)
#-------------------------------------------
#function to calculate difference of two number
def calculate_difference(num1,num2):
    return (num1-num2)
#-------------------------------------------
#function to calculate multiplication of two number
def calculate_multiplication(num1,num2):
    return (num1*num2)
#-------------------------------------------
#function to calculate division of two number
def calculate_division(num1,num2):
    return (num1/num2)
#-------------------------------------------
#function to calculate remainder of two number
def calculate_remainder(num1,num2):
    return (num1%num2)
#-------------------------------------------